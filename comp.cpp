CERTCertificate* makeCertificate(const uint8_t* der, unsigned int len) {
  auto db = CERT_GetDefaultCertDB();
  SECItem certItem{
        siDERCertBuffer,
        (unsigned char *)der,
        len};
  auto cert = CERT_NewTempCertificate(db, &certItem, nullptr, false, false);
  return cert;
}

SECStatus validateCertificate(CERTCertificate* cert) {
  auto db = CERT_GetDefaultCertDB();
  SECCertificateUsage ret;
  auto status = CERT_VerifyCertificateNow(db, cert, true, certUsageSSLServer, nullptr, &ret);
  std::cout << status << std::endl;
  if (status != SECSuccess) {
    PRErrorCode err_code = PR_GetError();
    auto err_text = std::unique_ptr<char>(new char);
    auto bytes_read = PR_GetErrorText(err_text.get());
    if (bytes_read != 0) {
      std::cout << "The cert verifier goes: " << err_text << std::endl;
    } else {
      std::cout << "got status " << err_code << " from NSS_Init but couldn't get the error text" << std::endl;
    }
    exit(err_code);
  }
  return status;
}

